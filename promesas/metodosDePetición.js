// Método Get: //
// consultar y traer información ejm: información de saldo de una cuenta de banco //

// Método Post //
// agregar, añadir o enviar información ejm: relleno de un formulario

// Método Delete//
// borrar un dato en base a la información entregada

// Método Put // 
// reemplaza los campos de una base de datos (debe ser si o si modificable el elemento)

// Método Patch //
// solo puedo reemplazar un elemento de la base de datos (ejm: nombre de algún pokemon)